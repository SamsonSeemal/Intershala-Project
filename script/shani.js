  function display1Data(data) {
    data.map(function (elem) {
      var ancorTag = document.createElement('a');
      ancorTag.href = "https://www.google.com/";
  
      var firstDiv = document.createElement('div');
      firstDiv.setAttribute('class', 'item');
  
      var image = document.createElement('img');
      image.setAttribute('class', 'image');
      image.src = elem.image;
  
      var title = document.createElement('h4');
      title.setAttribute('class', 'title');
      title.innerText = elem.title;
  
      var description = document.createElement('p');
      description.setAttribute('class', 'description');
      description.innerText = elem.description;
  
      var secondDiv = document.createElement('div');
      secondDiv.setAttribute('class', 'secondDiv');
  
      var thirdDiv = document.createElement('div');
      thirdDiv.setAttribute('class', 'thirdDiv');
      thirdDiv.innerText = 'Duration: ' + elem.durations;
  
      var fourthDiv = document.createElement('div');
      fourthDiv.setAttribute('class', 'fourthDiv');
      fourthDiv.innerText = elem.knowMore;
  
      secondDiv.append(thirdDiv, fourthDiv);
  
      firstDiv.append(image, title, description, secondDiv);
  
      ancorTag.append(firstDiv);
  
      document.querySelector('#div1').append(ancorTag);
    });
  }
  
